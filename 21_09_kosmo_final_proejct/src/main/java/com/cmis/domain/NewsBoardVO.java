package com.cmis.domain;

public class NewsBoardVO {
	private int news_num;
	private String news_title;
	private String news_link;
	private int news_hits;
	private String news_keyword;
	private String news_press;
	private String news_content;
	private String news_img;
	
	public int getNews_num() {
		return news_num;
	}
	public void setNews_num(int news_num) {
		this.news_num = news_num;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNews_link() {
		return news_link;
	}
	public void setNews_link(String news_link) {
		this.news_link = news_link;
	}
	public int getNews_hits() {
		return news_hits;
	}
	public void setNews_hits(int news_hits) {
		this.news_hits = news_hits;
	}
	public String getNews_keyword() {
		return news_keyword;
	}
	public void setNews_keyword(String news_keyword) {
		this.news_keyword = news_keyword;
	}
	public String getNews_press() {
		return news_press;
	}
	public void setNews_press(String news_press) {
		this.news_press = news_press;
	}
	public String getNews_content() {
		return news_content;
	}
	public void setNews_content(String news_content) {
		this.news_content = news_content;
	}
	public String getNews_img() {
		return news_img;
	}
	public void setNews_img(String news_img) {
		this.news_img = news_img;
	}
	
}
